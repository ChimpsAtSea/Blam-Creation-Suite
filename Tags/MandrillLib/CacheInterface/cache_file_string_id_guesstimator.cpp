c_cache_file_string_id_guesstimator::c_cache_file_string_id_guesstimator(c_cache_file& cache_file) :
	cache_file(cache_file),
	cache_file_string_id_start_index(0),
	engine_string_id_first_set_end_index(0),
	engine_string_id_last_set_end_index(0),
	set_count(0),
	string_id_set_ranges(),
	string_id_set_start_indices(),
	string_id_set_end_indices()
{
	set_count = _countof(engine_string_id_guesstimator_data);

	uint32_t current_search_index = 0;

	for (uint32_t set_index = 0; set_index < _countof(engine_string_id_guesstimator_data); set_index++)
	{
		s_engine_string_id_guesstimator_data* const set_guesstimator_data = engine_string_id_guesstimator_data[set_index].first;
		uint32_t const set_guesstimator_count = engine_string_id_guesstimator_data[set_index].second;
		if (set_guesstimator_count == 0)
		{
			if (c_command_line::has_command_line_arg("-guesstimatordebug"))
			{
				c_console::write_line_verbose("string_id_guesstimator> set %u [empty]", set_index);
			}
			continue;
		}

		uint32_t start_string_index = current_search_index;

		for (uint32_t i = 0; i < set_guesstimator_count; i++)
		{
			s_engine_string_id_guesstimator_data& guesstimator_data = set_guesstimator_data[i];
			const char* const guesstimator_string = guesstimator_data.string;

			// search for the beginning
			for (; start_string_index < cache_file.get_string_id_count(); start_string_index++)
			{
				const char* const cache_file_string = cache_file.get_string_id_by_index(start_string_index);

				if (strcmp(cache_file_string, guesstimator_string) == 0)
				{
					goto search_for_set_end;
				}
			}

			if (!guesstimator_data.is_reliable)
			{
				break; // #TODO: This data is unreliable
			}
		}
	search_for_set_end:

		uint32_t end_string_index = start_string_index;

		for (uint32_t i = set_guesstimator_count - 1; i < set_guesstimator_count; i--)
		{
			s_engine_string_id_guesstimator_data& guesstimator_data = set_guesstimator_data[i];
			const char* const guesstimator_string = guesstimator_data.string;

			// search for the beginning
			for (; end_string_index < cache_file.get_string_id_count(); end_string_index++)
			{
				const char* const cache_file_string = cache_file.get_string_id_by_index(end_string_index);

				if (strcmp(cache_file_string, guesstimator_string) == 0)
				{

					goto finish_search;
				}
			}

			if (!guesstimator_data.is_reliable)
			{
				break; // #TODO: This data is unreliable
			}
		}
	finish_search:

		end_string_index++;
		current_search_index = end_string_index;

		if (c_command_line::has_command_line_arg("-guesstimatordebug"))
		{
			c_console::write_line_verbose("string_id_guesstimator> set %u [%u - %u]", set_index, start_string_index, end_string_index);
		}

		string_id_set_start_indices[set_index] = start_string_index;
		string_id_set_end_indices[set_index] = end_string_index;
		string_id_set_ranges[set_index] = end_string_index - start_string_index;
	}

	// #TODO Smartly determine which value to use
	engine_string_id_first_set_end_index = string_id_set_end_indices[0];
	engine_string_id_last_set_end_index = string_id_set_end_indices[set_count - 1];
	cache_file_string_id_start_index = engine_string_id_last_set_end_index - engine_string_id_first_set_end_index;

	const char* expected_value = cache_file.get_string_id_by_index(engine_string_id_last_set_end_index);
	if (expected_value == nullptr || strcmp(expected_value, "material") != 0)
	{
		c_console::write_line_verbose("string_id_guesstimator> Unexpected cache file String ID at index 0. Potential errors in the guesstimator!");
	}
}

